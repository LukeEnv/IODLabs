function Plus(a, b) {
  return a + b;
}

function Minus(a, b) {
  return a - b;
}

function Multiply(a, b) {
  return a * b;
}

function Divide(a, b) {
  return a / b;
}

function Greetings(name) {
  return "Hello " + name;
}

console.log(Plus(3, 2));
console.log(Minus(8, 2));
console.log(Multiply(15, 4));
console.log(Divide(20, 2));
console.log(Greetings("Luke"));
